def iniciar_udp():
    addr = socket.getaddrinfo("0.0.0.0", UDP_PORT)[0][-1]
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.bind(addr)
    sock.settimeout(0.05)  # 50ms para respuesta rápida sin bloqueos
    print("Servidor UDP escuchando en puerto", UDP_PORT)
    return sock

def iniciar_multicast():
    return socket.socket(socket.AF_INET, socket.SOCK_DGRAM)

def enviar_multicast(sock, mensaje):
    sock.sendto(mensaje.encode("utf-8"), (MULTICAST_GROUP, MULTICAST_PORT))

def procesar_mensaje(mensaje):
    mensaje = mensaje.strip().upper()

    if mensaje == "LED_ON":
        led.value(1)
        return "ACK:LED_ON"

    elif mensaje == "LED_OFF":
        led.value(0)
        return "ACK:LED_OFF"

    elif mensaje == "STATUS":
        estado = "ON" if led.value() else "OFF"
        return "STATUS:LED={}".format(estado)
    else:
        return "ERROR:COMANDO_DESCONOCIDO"


# =========================
def obtener_temperatura():
    sensor.measure()
    temp = sensor.temperature()
    hum = sensor.humidity()
    return temp, hum

# =========================
def mostrar_emoji(temp, hum):
    oled.fill(0)
    oled.text("Temp: {:.1f}C".format(temp), 0, 0)
    oled.text("Hum: {:.1f}%".format(hum), 0, 10)

    if temp < 15:
        oled.text("FRIO [*]", 30, 30)
    elif temp >= 15 and temp <= 30:
        oled.text("NORMAL :)", 30, 30)
    else:
        oled.text("CALOR [!]", 30, 30)

    estado_led = "ON" if led.value() else "OFF"
    oled.text("LED: {}".format(estado_led), 0, 50)

    oled.show()

